function changeBackgroundColor() {
    // Chọn phần tử cần thay đổi màu nền (ở đây là body, bạn có thể thay đổi thành phần tử khác)
    var paragraph = document.getElementById("mausac");
    // Chọn một màu mới (ví dụ: đỏ) để thay đổi
    var newColor = "#ff0000";
    // Thiết lập màu nền mới cho phần tử
    paragraph.style.backgroundColor = newColor;
    var paragraph = document.getElementById("mausac");
    document.getElementById("mausac").innerHTML= "Màu đỏ";
  }


